void main() {
  //DATA TYPES

  //String
  String name = 'John';
  print('Data Type String: $name');

//Number
  int year = 2024;
  print('this $year is year for change');

//Double (numbers with decimal)

  double weight = 65.64;
  print('Total size of gas: $weight');

// Boolean (Yes or No option)
  bool wrong = true;
  print('Is it wrong?: $wrong');

  String vacationPlace = 'Vernice';
  print('$vacationPlace is a lovely city to visit again');
}
